    # Find and display the minimum and maximum grades
    # Sort the grades in ascending order

# Create a list with 7 exam grades (between 0 and 20)
grade_list = [10, 17, 12, 11, 8, 12, 19]

# Calculate and display the average grade
total = sum(grade_list)
print(total / len(grade_list))

# Find and display the minimum and maximum grades
print(min(grade_list))
print(max(grade_list))

# Sort the grades in ascending order
print(sorted(grade_list))


# Takeaway of the exercise
    # This exercise demonstrates how to perform basic analysis on a list of numerical values in Python. 
    # It covers calculating the average using sum and len, finding the minimum and maximum values with min and max, and sorting the list in ascending order with sorted. 
